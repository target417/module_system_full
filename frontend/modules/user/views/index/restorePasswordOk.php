<h1>Востановление пароля</h1>

<p>Все пароли хранятся в зашифрованном виде. Поэтому мы не можем предоставить вам ваш старый пароль. Вместо этого на ваш почтовый ящик было отправлено письмо с новым, случайно сгенерированным паролем.</p>
<p>Используйте его для входа на сайт. Обязательно смените пароль. Сделать это можно в разделе <b>"Редактирование личных данных"</b>.</p>